# Задача 1: Сумма трех чисел
print("=== Задача 1 ===")
a = int(input("Введи первое число: "))
b = int(input("Введи второе число: "))
c = int(input("Введи третье число: "))
print(f"Сумма: {a + b + c}")
print()

# Задача 2: Площадь прямоугольников
print("=== Задача 2 ===")
length = int(input("Введи длину: "))
width = int(input("Введи ширину: "))
print(f"Площадь: {length * width}")
print()

# Задача 3: Следующее и предыдущее
print("=== Задача 3 ===")
n = int(input("Введи число: "))
print(f"Следующее: {n + 1}")
print(f"Предыдущее: {n - 1}")
print()

# Задача 4: Деление нацело и остаток
print("=== Задача 4 ===")
a = int(input("Введи первое число: "))
b = int(input("Введи второе число: "))
print(f"Целая часть от деления: {a // b}")
print(f"Остаток: {a % b}")
print()

# Задача 5: Прибавить 10%
